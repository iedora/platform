/**
 * Append-only platform audit log. Every meaningful admin / identity action
 * inserts one row here from inside its server action; the `/admin/audit`
 * page reads it back with filtering + pagination.
 *
 * Distinct in audience from `webhook_subscription` deliveries: webhooks
 * notify external products, this table is the compliance / review trail
 * for human operators. Writes are NEVER swallowed — if the insert fails
 * the originating action must fail too (see `features/audit/sender.ts`).
 *
 * `actorId` is `set null` on user delete so the trail survives a deleted
 * admin account. The actor's role at the time of the event is snapshotted
 * in `actorRole` for the same reason.
 */
export const auditLog = pgTable(
  "audit_log",
  {
    id: text("id").primaryKey(),
    actorId: text("actor_id").references(() => user.id, {
      onDelete: "set null",
    }),
    actorRole: text("actor_role"),
    action: text("action").notNull(),
    targetType: text("target_type"),
    targetId: text("target_id"),
    payload: jsonb("payload"),
    ip: text("ip"),
    userAgent: text("user_agent"),
    occurredAt: timestamp("occurred_at").defaultNow().notNull(),
    /**
     * Tamper-evident chain. `rowHash` is sha256(prev_hash · actor_id ·
     * actor_role · action · target_type · target_id · canonical(payload) ·
     * ip · user_agent · occurred_at) — see `features/audit/chain.ts`.
     *
     * `prevHash` is NULL on the genesis row, otherwise the previous row's
     * `rowHash`. Together they form a chain that auditors verify end-to-end:
     * a single byte mutated in any column breaks the chain at that row and
     * every row after.
     *
     * Backfill: rows that pre-date this migration are NULL for `prev_hash`
     * + `row_hash` until `scripts/backfill-audit-chain.mjs` populates them.
     * After backfill `rowHash` is effectively NOT NULL — we don't enforce
     * at the schema layer to keep the deploy + backfill ordering loose
     * (deploy the migration, run the backfill, then a follow-up migration
     * can tighten NOT NULL if we ever want to).
     *
     * No index — hashes are random, no useful query selectivity. The
     * verification walk uses the existing `occurred_at` index.
     */
    prevHash: text("prev_hash"),
    rowHash: text("row_hash"),
  },
  (table) => [
    index("audit_log_actor_idx").on(table.actorId),
    index("audit_log_target_idx").on(table.targetType, table.targetId),
    index("audit_log_action_idx").on(table.action),
    index("audit_log_occurred_idx").on(table.occurredAt),
  ],
);

/**
 * Webhook subscriptions for the iedora identity bus. Genkan's webhooks
 * slice reads this table at delivery time and POSTs the signed envelope
 * to every enabled subscriber whose `events` allow-list covers the event
 * being emitted (NULL = receive everything).
 */
export const webhookSubscription = pgTable(
  "webhook_subscription",
  {
    id: text("id").primaryKey(),
    /** Absolute HTTPS endpoint the subscriber POSTs to. */
    url: text("url").notNull(),
    /** Shared HMAC secret. Both sides sign + verify with it. */
    secret: text("secret").notNull(),
    /** NULL = subscribe to every event; otherwise an explicit allow-list. */
    events: text("events").array(),
    /** Soft-disable without losing the row. */
    enabled: boolean("enabled").default(true).notNull(),
    /** Free-form label shown in admin UI (e.g. "menu"). */
    name: text("name"),
    createdAt: timestamp("created_at").defaultNow().notNull(),
    updatedAt: timestamp("updated_at")
      .defaultNow()
      .$onUpdate(() => /* @__PURE__ */ new Date())
      .notNull(),
  },
  (table) => [index("webhookSubscription_enabled_idx").on(table.enabled)],
);

