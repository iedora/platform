// stableAuthSecret keeps `IEDORA_CORE_SECRET` stable across orchestrator
// runs. If `.env.local` already pins one, reuse it; otherwise mint 48
// bytes (the operator can persist it by copying into `.env.local`).
func stableAuthSecret(envLocalPath string) string {
	if v, ok := parseEnvLocal(envLocalPath)["IEDORA_CORE_SECRET"]; ok && v != "" {
		return v
	}
	buf := make([]byte, 48)
	if _, err := rand.Read(buf); err != nil {
		fail("mint core secret: %v", err)
	}
	return base64.RawStdEncoding.EncodeToString(buf)
}

func writeEnvFile(path string, env map[string]string) {
	keys := make([]string, 0, len(env))
	for k := range env {
		keys = append(keys, k)
	}
	sort.Strings(keys)

	var b strings.Builder
	b.WriteString("# AUTO-GENERATED by `go run ./dev/cmd/local-stack`.\n")
	b.WriteString("# Overrides live in `.env.local` (gitignored).\n\n")
	for _, k := range keys {
		fmt.Fprintf(&b, "%s=%s\n", k, env[k])
	}
	if err := os.WriteFile(path, []byte(b.String()), envFileMode); err != nil {
		fail("write %s: %v", path, err)
	}
}

func parseEnvLocal(path string) map[string]string {
	out := map[string]string{}
	body, err := os.ReadFile(path)
	if err != nil {
		return out
	}
	for _, line := range strings.Split(string(body), "\n") {
		line = strings.TrimSpace(line)
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		k, v, ok := strings.Cut(line, "=")
		if !ok {
			continue
		}
		out[strings.TrimSpace(k)] = strings.TrimSpace(v)
	}
	return out
}

// warnEnvLocalState surfaces a notice when `.env.local` shadows a key
// the orchestrator just composed (operator override + auto-composed
// value disagree). Pure observability — never mutates.
func warnEnvLocalState(envLocalPath string, selected []string) {
	existing := parseEnvLocal(envLocalPath)
	if len(existing) == 0 {
		return
	}
	excludeSet := map[string]bool{}
	for _, k := range excludedEnvKeys(selected) {
		excludeSet[k] = true
	}
	var shadowing []string
	for k, v := range existing {
		if strings.TrimSpace(v) == "" {
			continue
		}
		if !excludeSet[k] {
			shadowing = append(shadowing, k)
		}
	}
	if len(shadowing) > 0 {
		sort.Strings(shadowing)
		fmt.Printf("%s ⚠ .env.local shadows %d auto-composed key(s): %s\n",
			logPrefix, len(shadowing), strings.Join(shadowing, ", "))
	}
}
