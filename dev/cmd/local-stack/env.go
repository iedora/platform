// writeMenuEnvFiles composes `products/menu/.env` from local-stack
// statics. Excluded services drop out of the env entirely — operators
// who want to point a key at a remote URL hand-edit `.env.local`
// (gitignored, never touched by the orchestrator).
//
// All values here are well-known constants for the local docker network
// (`infra-postgres:5432`, `infra-openobserve:5080`,
// `infra-localstack:4566`). No Tofu output reads, no app-state outputs
// — the orchestrator owns the recipe end-to-end.
func writeMenuEnvFiles(envPath, envLocalPath string, selected []string) {
	env := composeMenuEnv(envLocalPath)

	excludeSet := map[string]bool{}
	for _, k := range excludedEnvKeys(selected) {
		excludeSet[k] = true
	}
	envForFile := map[string]string{}
	for k, v := range env {
		if !excludeSet[k] {
			envForFile[k] = v
		}
	}
	writeEnvFile(envPath, envForFile)
}

// composeMenuEnv returns the full menu env map for the local stack.
